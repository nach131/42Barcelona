#include <stdio.h>
#include <stdlib.h>

void job(int *x)
{
	int j = 7;
	*x = j;
	printf("%d-",*x);
} 

int main()
{
	int i = 66;
	int *x;
	x = &i;
	job(x);
	printf("%d",*x);

	return 0;

}
