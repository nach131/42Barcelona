#include <stdio.h>
#include <unistd.h>


static void	ft_cambia(char *s)
{
	
	s[1] = 't';
	printf("\t %s\n", s);
}

int	main(void)
{
	char	str[] = "42 Barcelona";

	ft_cambia(str);
	printf("%s\n", str);
}

