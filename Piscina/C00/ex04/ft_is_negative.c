#include <unistd.h>

void	ft_is_negative(int n)
{
	int	i_n;
	int	i_p;

	i_n = 'N';
	i_p = 'P';
	if (n < 0)
	{
		write(1, &i_n, 1);
	}		
	else
	{
		write(1, &i_p, 1);
	}
}	
