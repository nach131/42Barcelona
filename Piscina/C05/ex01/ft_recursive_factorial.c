// recursiva ==> que la misma funcion se llame a ella misma hasta finalizar
// el while

int	ft_recursive_factorial(int nb)
{
	int	resultado;

	resultado = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	return (nb * ft_recursive_factorial(nb - 1));
}
