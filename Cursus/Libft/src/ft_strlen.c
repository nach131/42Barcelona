// La función strlen() toma una cadena como argumento y devuelve su longitud.
// El valor devuelto es de tipo size_t (el tipo entero sin signo).

#include "libft.h"

int	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}
