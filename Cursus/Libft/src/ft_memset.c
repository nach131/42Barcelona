#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*ch;
	size_t	i;

	ch = str;
	i = 0;
	while (i < n)
	{
		ch[i] = c;
		i++;
	}
	return (ch);
}
