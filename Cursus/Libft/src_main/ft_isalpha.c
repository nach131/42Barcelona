#include <stdio.h>
#include <unistd.h>

int ft_isalpha(int c)
{
printf("isalpha %d\n",c);

	if ((c >=65 && c <= 90) || (c >=97 && c <= 122)){
	return 1;
	} else 
return 0;
}

