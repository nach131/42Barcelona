#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char	*sr;

	sr = (unsigned char *)str;
	while (n)
	{
		if (*sr == (unsigned char)c)
			return (sr);
		else
			sr++;
			n--;
	}
	return (NULL);
}
